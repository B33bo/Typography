        public static string Encode(string Input)
        {
            ProgressBar bar = new ProgressBar("Big Letters", Input.Length);
            string returnValue = "";
            for (int i = 0; i < Input.Length; i++)
            {
                bar.Increase();

                if (letters.ContainsKey(Input[i].ToString()))
                    returnValue += letters[Input[i].ToString()].Replace("NULL", "") + "\n\n";
                else if (Input[i] == ' ' || Input[i] == '\n' || Input[i] == '\r')
                    returnValue += "\n\n\n";
                else
                {
                    Program.Error($"{Input[i]} cannot be enlarged");
                    returnValue += Input[i] + "\n\n";
                }    
            }

            return returnValue;
        }
